        <p style="padding-top: 60px;">一、VI设计的内涵</p>
        <p>
            VI（视觉识别Visual Identity）以标志、标准字、标准色为核心展开的完整的、系统的视觉表达体系。是CIS系统最具传播力和感染力的部分，将CI的非可视化内容转化为静态的视觉识别符号，以无比丰富的多彩的应用形式，在最为广泛的层面上，进行最直接的传播。
            20世纪50年代前后，在美国兴起视觉识别（VI）。随着现代化、工业化、自动化的发展，市场竞争也变得更加激烈。另外，各种媒体的急速膨胀，传播途径不一而从，受众面对大量繁杂的信息，变得无所适从。企业比任何时候都需要统一的、集中的VI设计传播，个性和身份的识别因此显得由为重要。设计到位、实施科学的视觉识别系统，是传播企业经营理念、建立企业知名度、塑造企业形象的快速便捷之途；企业可以通过VI设计对内征得员工的认同感，归属感，加强企业凝聚力；对外树立企业的整体形象，资源整合，有控制的将企业的信息传达给受众，通过视觉符码，不断的强化受众的意识，从而获得认同。
        </p>
        <p>二、VI设计在中国的发展和未来趋势</p>
        <p>标志设计发展到近代已经相当成熟，主要体现在其艺术性、标准性、应用性。今天，我们根据设计行业服务的经验来分析，VI设计的更是冲破了某些束缚、更大胆、更前卫、更能掠夺受众的视线：</p>
        <p>
            1、图形的应用越来越少：企业运用图形越来越少了。因为企业标识的主要功能就是传递给受众信息，告诉受众“我是谁”，而图形的应用，却让传播多了一道障碍。因为，“传播不在于你放进去什么，而在于看到的人从里面拿出来什么”。这个意义的图形，在受众那里，却可能是别的意思。所以，图形标志中的应用越来越少了。
        </p>
        <p>2、标识主要由文字组成，文字多是中英文的搭配：因为标识的信息传播意义，文字是更好传递的工具；而英文的使用，VI设计让企业的标识能够更好的体现国际化的特点；</p>
        <p>3、文字标识的图形化：因为现在是读图时代，将文字图形化，更好的保持了企业形象的统一，也更好的传递了企业的信息。</p>
        <p>4、设计越来越简单化：很清楚，越是简单的东西，其传播力和影响力越强。</p>
        <p>三、VI设计的原则</p>
        <p>在VI设计之初，我们站在战略的高度，在进行VI策划设计之时，也以同一性、差异性、民族性、有效性作为指导：</p>
        <p>1、同一性</p>
        <p>
            企业形象对外传播的一致性与一贯性，应该运用统一设计和统一大众传播，用完美的视觉一体化设计，将信息与认识个性化、明晰化、有序化，把各种形式传播媒本上的形象统一，创造能储存与传播的统一的企业理念与视觉形象，这样才能集中与强化企业形象，使信息传播更为迅速有效，给社会大众留下强烈的印象与影响力。
            对企业识别的各种要素，从企业理念到视觉要素予以标准化，采同一的规范设计，对外传播均采同一的模式，并坚持长期一贯的运用，不轻易进行变动。
            同一性原则的运用能使社会大众对特定的企业形象有一个统一完整的认识，不会因为企业形象的识别要素的不统一而产生识别上的障碍，增强了形象的传播力。
        </p>
        <p>2、差异性</p>
        <p>
            企业形象为了能获得社会大众的认同，必须是个性化的、与众不同的，因此差异性的原则十分重要。
            差异性首先表现在不同行业的区分，因为，在社会性大众心目中，不同行业的企业与机构均有其行业的形象特征，如化妆品企业与机械工业企业的企业形象特征应是截然不同的。在设计时必须突出行业特点，才能使其与其它它行业有不同的形象特征，有利于识别认同。其次必须突出与同行业其它企业的差别，才能独具风采，脱颖而出。
            日本享誉世界的五大名牌电器企业：索尼、松下、东芝、三洋、日立，其企业形象均别具一格，十分个性化，有效地获得了消费大众的认同，在竞争激烈的世界家电市场上独树一帜。
        </p>
        <p>3、民族性</p>
        <p>
            形象的塑造与传播应该依据不同的民族文化，美、日等许多企业的崛起和成功，民族文化是其根本的驱动力。美国企业文化研究专家秋尔和肯尼迪指出：“一个强大的文化几乎是美国企业持续成功的驱动力。”驰名于世的“麦当劳”和“肯德基”独具特色的企业形象，展现的就是美国生活方式的快餐文化。
            塑造能跻身于世界之林的中国企业形象，必须弘扬中华民族文化优势，灿烂的中华民族文化，是我们取之不尽，用之不竭的源泉，有许多我们值得吸收的精华，有助于我们创造中华民族特色的企业形象。
        </p>
        <p>4、有效性</p>
        <p>
            有效性是指企业经策划与设计的VI计划能得以有效地推行运用，VI是解决问题学，不是企业的装扮物，因此能够操作和便于操作，其可操作性是一个十分重要的问题。  
            企业VI计划要具有效性，能够有效地发挥树立良好企业形象的作用，首先在其策划设计必须根据企业自身的情况，企业的市场营销的地位，在推行企业形象战略时确立准确的形象定位，然后以此定位进行发展规划。在这点上协助企业导入VI计划的机构或个人负有重要的职责，一切必须从实际出发，不能迎合企业领导人一些不切合实际的心态。笔者在协助产品在市场信息上刚有较好走势但实力并不雄厚的一家企业导入VI计划
        </p>
